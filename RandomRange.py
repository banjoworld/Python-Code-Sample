# Problem 1: Print 10 random integers between 25 and 35.

import random

# Loop 10 times to generate and print random numbers in the specified range
for _ in range(10):
    # random.randrange(25, 36) generates a random integer between 25 and 35 inclusive
    print(random.randrange(25, 36))